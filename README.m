GAIO 3.0

A Matlab toolbox for the global analysis of invariant objects
in dynamical systems.


Installation
------------

Unpack the archive, i.e.

> unzip GAIO-3.0.zip

and add the resulting folder to your Matlab path, e.g.

>> addpath(genpath('/Users/junge/GAIO-3.0'))


